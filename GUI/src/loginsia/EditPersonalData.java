    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        try {       
            CallableStatement sp1 = conexion.prepareCall("{CALL sp_est_actualizar_datos(?,?,?,?,?,?,?)}");
           sp1.setString(1,this.usuario);
            sp1.setString(2, CiudadText.getText());
            sp1.setString(3, CalleText.getText());
            sp1.setString(4, NumeroText.getText());
            sp1.setLong(5, Long.parseLong(TelCelText.getText()));
            sp1.setLong(6, Long.parseLong(TelText.getText()));
            sp1.setString(7, CorreoText.getText());
            sp1.executeQuery();
            JOptionPane.showMessageDialog(null,"Información actualizada correctamente.");
            jButton1.doClick(30);
            PersonalData data = new PersonalData(this.usuario,this.contrasena);
            Ventana.removeAll();
            Ventana.updateUI();
            Ventana.add(data);
            data.setVisible(true);
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage());
        }
    }//GEN-LAST:event_jButton1MouseClicked


