    private void obtenerDatosAct(String usuario){
        try {      
            CallableStatement sp1 = conexion.prepareCall("{CALL sp_info_personal_act(?)}");
            sp1.setString(1, usuario);
            ResultSet rs = sp1.executeQuery();
            if (rs.next()){
                CiudadText.setText(rs.getString("res_ciudad"));
                CalleText.setText(rs.getString("res_calle"));
                NumeroText.setText(rs.getString("res_numero"));
                TelCelText.setText(rs.getString("per_telefono_cel"));
                TelText.setText(rs.getString("per_telefono"));
                CorreoText.setText(rs.getString("per_correo_personal"));
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null,"Error: "+e.getMessage());
        }
    }

